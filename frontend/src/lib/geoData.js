export function normalizeCountryKey(str) {
  if (!str) return '';
  const lower = str.toLowerCase().trim();
  return (ALIASES[lower] || str).toLowerCase();
}

// ── Get country color ─────────────────────────────────────────────────────────
export function getCountryColor(country) {
  if (!country) return '#6366f1';
  const key = country.toLowerCase().trim();
  if (PRESET_COLORS[key]) return PRESET_COLORS[key];
  let h = 5381;
  for (const c of key) { h = ((h << 5) + h) + c.charCodeAt(0); h = h & h; }
  const hue = Math.abs(h) % 360;
  return `hsl(${hue}, 80%, 62%)`;
}
