// ── Seeded random for consistent per-user jitter ──────────────────────────────
function seededRand(seed) {
  let s = (seed || 1234567) >>> 0;
  return () => {
    s ^= s << 13; s ^= s >> 17; s ^= s << 5;
    return ((s >>> 0) / 0xffffffff);
  };
}

// ── Get lat/lng for a user (deterministic) ────────────────────────────────────
export function getCoords(city, country, userId) {
  const ck = city?.toLowerCase().trim();
  const cok = country?.toLowerCase().trim();
  const seed = userId ? parseInt(userId.slice(-8), 16) : Math.random() * 1e8;
  const rand = seededRand(seed);

  let base = null;
  let jitter = 0.25;

  if (ck && CITY_COORDS[ck]) {
    base = CITY_COORDS[ck];
    jitter = 0.18;
  } else if (cok && COUNTRY_COORDS[cok]) {
    base = COUNTRY_COORDS[cok];
    jitter = 1.8;
  } else {
    return { lat: (rand() - 0.5) * 140, lng: (rand() - 0.5) * 340 };
  }

  return {
    lat: base.lat + (rand() - 0.5) * jitter * 2,
    lng: base.lng + (rand() - 0.5) * jitter * 2,
  };
}

